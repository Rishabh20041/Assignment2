#Identity Operators:

#is

a1 = [1, 2, 3]
b1 = a1
c1 = [1, 2, 3]
print(a1 is b1)  
print(a1 is c1)  

#isNot

a1 = [1, 2, 3]
b1 = a1
c1 = [1, 2, 3]
print(a1 is not b1)  
print(a1 is not c1)  

#Membership Operators:

#in

colour = ['RED', 'BLUE', 'GREEN']
print('BLUE' in colour)    
print('BLACK' in colour)     

#not in

colour = ['RED', 'BLUE', 'GREEN']

print('BLUE' not in colour) 
print('BLACK' not in colour)  
